package test11;

public class Buyer {
	int ten;
	int twen;
	int fif;
	int hun;
	int t;
	public Buyer(int ten, int twen, int fif, int hun, int t) {
		super();
		this.ten = ten;
		this.twen = twen;
		this.fif = fif;
		this.hun = hun;
		this.t = t;
	}
	public int getTen() {
		return ten;
	}
	public int getTwen() {
		return twen;
	}
	public int getFif() {
		return fif;
	}
	public int getHun() {
		return hun;
	}
	public int getT() {
		return t;
	}
	
	}


