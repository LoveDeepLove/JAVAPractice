package test11;

public class Seller {
	int ten;
	int twen;
	int fif;
	int hun;
	public Seller(int ten, int twen, int fif, int hun) {
		super();
		this.ten = ten;
		this.twen = twen;
		this.fif = fif;
		this.hun = hun;
	}
	public int getTen() {
		return ten;
	}
	public int getTwen() {
		return twen;
	}
	public int getFif() {
		return fif;
	}
	public int getHun() {
		return hun;
	}
	
	

}
